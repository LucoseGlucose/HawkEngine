    public class EditorFont
    {
        private readonly List<EditorUtils.EditorFontData> fonts = new();

        public EditorFont(params EditorUtils.EditorFontData[] fonts)
        {
            this.fonts = fonts.ToList();
        }
        public ImFontPtr? this[EditorUtils.FontStyle style, EditorUtils.FontSize size]
        {
            get
            {
                EditorUtils.EditorFontData? font = fonts.Where(f => f.fontStyle == style && f.fontSize == size).FirstOrDefault();

                if (!font.HasValue) return null;
                return font.Value.fontPtr;
            }
        }
        public unsafe void SetAsDefault()
        {
            EditorUtils.EditorFontData? font = fonts.Where(f => f.fontStyle == EditorUtils.FontStyle.Regular
                && f.fontSize == EditorUtils.FontSize.Medium).FirstOrDefault();
            if (!font.HasValue) return;

            EditorGUI.io.NativePtr->FontDefault = font.Value.fontPtr;
        }
    }

